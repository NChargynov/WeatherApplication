package com.geektech.weatherapplication.ui.onboard;

public class OnBoardItem {

    private String title;
    private int image;

    public OnBoardItem(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }


    public int getImage() {
        return image;
    }

}
